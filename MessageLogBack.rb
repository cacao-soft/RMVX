=begin

 -- 概    要 ----------------------------------------------------------------

  ログウィンドウを非表示にして、背景画像を表示します。

 -- 注意事項 ----------------------------------------------------------------

  ※ 開閉時のアニメーション表示は無効になります。

 -- 画像規格 ----------------------------------------------------------------

  ★ 背景画像
   画像は Graphics/Pictures フォルダに入れてください。


=end


#==============================================================================
# ◆ 設定項目
#==============================================================================
class Window_MessageLog
  #--------------------------------------------------------------------------
  # ◇ 背景画像のファイル名
  #--------------------------------------------------------------------------
  #   nil .. 画像を使用しない
  #--------------------------------------------------------------------------
  BACKGROUND_FILE = ""
end


#/////////////////////////////////////////////////////////////////////////////#
#                                                                             #
#                下記のスクリプトを変更する必要はありません。                 #
#                                                                             #
#/////////////////////////////////////////////////////////////////////////////#


class Window_MessageLog
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  alias _cao_log_plus_initialize initialize
  def initialize
    @background = Sprite.new
    @background.z = 10100
    @background.bitmap = Cache.picture(BACKGROUND_FILE) if BACKGROUND_FILE
    @background.visible = false
    _cao_log_plus_initialize
    self.opacity = 0
  end
  #--------------------------------------------------------------------------
  # ● ウィンドウを開く
  #--------------------------------------------------------------------------
  def open
    @background.visible = true
    self.openness = 255
  end
  #--------------------------------------------------------------------------
  # ● ウィンドウを閉じる
  #--------------------------------------------------------------------------
  def close
    @background.visible = false
    self.openness = 0
  end
end
